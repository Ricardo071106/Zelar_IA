import pkg from 'whatsapp-web.js';
const { Client, LocalAuth } = pkg;
import express from 'express';
import bodyParser from 'body-parser';
import qrcode from 'qrcode-terminal';
import { exec } from 'child_process';
import { promisify } from 'util';

const execAsync = promisify(exec);

// Configura√ß√£o do Express
const app = express();
app.use(bodyParser.json());

// Estado global
let isClientReady = false;
let qrCodeString = '';
let clientInfo = null;
let client = null;

// Fun√ß√£o para configurar o cliente WhatsApp
async function createClient() {
    // Detectar Chromium automaticamente
    let chromiumPath;
    try {
        const { stdout } = await execAsync('which chromium');
        chromiumPath = stdout.trim();
        console.log('üîç Chromium encontrado em:', chromiumPath);
    } catch (error) {
        console.log('‚ö†Ô∏è Chromium n√£o encontrado, usando configura√ß√£o padr√£o');
    }

    const clientConfig = {
        authStrategy: new LocalAuth({
            clientId: "zelar-whatsapp-bot",
            dataPath: "./whatsapp-session"
        }),
        puppeteer: {
            headless: true,
            args: [
                '--no-sandbox',
                '--disable-setuid-sandbox',
                '--disable-dev-shm-usage',
                '--disable-accelerated-2d-canvas',
                '--no-first-run',
                '--no-zygote',
                '--single-process',
                '--disable-gpu',
                '--disable-web-security',
                '--disable-features=VizDisplayCompositor',
                '--disable-background-timer-throttling',
                '--disable-renderer-backgrounding',
                '--disable-backgrounding-occluded-windows',
                '--disable-ipc-flooding-protection',
                '--disable-default-apps',
                '--disable-extensions',
                '--disable-sync',
                '--disable-translate',
                '--hide-scrollbars',
                '--mute-audio',
                '--no-default-browser-check',
                '--no-first-run',
                '--disable-plugins',
                '--disable-images'
            ]
        }
    };

    // Usar Chromium se dispon√≠vel
    if (chromiumPath) {
        clientConfig.puppeteer.executablePath = chromiumPath;
    }

    return new Client(clientConfig);
}

// =================== EVENTOS DO WHATSAPP ===================

async function setupClientEvents(client) {
    client.on('qr', (qr) => {
        console.log('\nüîó QR Code gerado! Escaneie com seu WhatsApp:');
        console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
        qrcode.generate(qr, { small: true });
        console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
        console.log('üì± Como conectar:');
        console.log('1. Abra o WhatsApp no seu celular');
        console.log('2. Toque em Menu (‚ãÆ) > Dispositivos conectados');
        console.log('3. Toque em "Conectar dispositivo"');
        console.log('4. Escaneie este QR Code\n');
        qrCodeString = qr;
    });

    client.on('ready', async () => {
        try {
            clientInfo = {
                pushname: client.info.pushname,
                number: client.info.wid.user,
                platform: client.info.platform
            };
            
            console.log('‚úÖ WhatsApp conectado com sucesso!');
            console.log(`üì± Conectado como: ${clientInfo.pushname}`);
            console.log(`üìû N√∫mero: ${clientInfo.number}`);
            console.log(`üíª Plataforma: ${clientInfo.platform}`);
            console.log('üöÄ Bot Zelar est√° pronto para receber mensagens!\n');
            isClientReady = true;
            qrCodeString = ''; // Limpar QR Code ap√≥s conex√£o
        } catch (error) {
            console.error('‚ùå Erro ao obter informa√ß√µes do cliente:', error.message);
            isClientReady = true; // Marcar como pronto mesmo assim
        }
    });

    client.on('authenticated', () => {
        console.log('üîê Autentica√ß√£o realizada com sucesso!');
        console.log('üíæ Sess√£o salva - n√£o ser√° necess√°rio escanear novamente');
    });

    client.on('auth_failure', (msg) => {
        console.error('‚ùå Falha na autentica√ß√£o:', msg);
        console.log('üîÑ Tentando reconectar...');
        isClientReady = false;
        qrCodeString = '';
    });

    client.on('disconnected', (reason) => {
        console.log('üì¥ Cliente desconectado:', reason);
        isClientReady = false;
        clientInfo = null;
        console.log('üîÑ Tentando reconectar em 5 segundos...');
        setTimeout(() => {
            console.log('üîÑ Reiniciando cliente WhatsApp...');
            initializeWhatsApp();
        }, 5000);
    });

    // Auto-resposta para mensagens recebidas
    client.on('message', async (message) => {
        try {
            // Obter informa√ß√µes do contato
            const contact = await message.getContact();
            const chatName = contact.pushname || contact.number;
            const isGroup = message.from.includes('@g.us');
            
            // Log da mensagem recebida
            console.log(`üì• Mensagem ${isGroup ? 'do grupo' : 'de'} ${chatName} (${contact.number}): "${message.body}"`);
            
            // Verificar se n√£o √© uma mensagem enviada por n√≥s e n√£o √© de grupo
            if (!message.fromMe && !isGroup) {
                try {
                    // Enviar auto-resposta
                    await message.reply('Ol√°, aqui √© o Zelar! ü§ñ\n\nObrigado por entrar em contato. Sou um assistente automatizado.');
                    console.log(`üì§ Auto-resposta enviada para ${chatName}: "Ol√°, aqui √© o Zelar!"`);
                } catch (error) {
                    console.error(`‚ùå Erro ao enviar auto-resposta para ${chatName}:`, error.message);
                }
            } else if (isGroup) {
                console.log('üìù Mensagem de grupo - auto-resposta desabilitada');
            }
        } catch (error) {
            console.error('‚ùå Erro ao processar mensagem:', error.message);
        }
    });

    client.on('message_create', async (message) => {
        // Log de mensagens enviadas por n√≥s
        if (message.fromMe) {
            try {
                const chat = await message.getChat();
                const chatName = chat.name || 'Contato';
                console.log(`üì§ Mensagem enviada para ${chatName}: "${message.body}"`);
            } catch (error) {
                console.log(`üì§ Mensagem enviada: "${message.body}"`);
            }
        }
    });
}

// =================== API REST ===================

// Endpoint para verificar status detalhado
app.get('/status', (req, res) => {
    res.json({
        success: true,
        status: isClientReady ? 'connected' : 'disconnected',
        clientInfo: clientInfo,
        hasQR: !!qrCodeString,
        timestamp: new Date().toISOString(),
        uptime: process.uptime()
    });
});

// Endpoint para obter QR Code
app.get('/qr', (req, res) => {
    if (isClientReady) {
        res.json({
            success: true,
            message: 'WhatsApp j√° est√° conectado',
            connected: true,
            clientInfo: clientInfo
        });
    } else if (qrCodeString) {
        res.json({
            success: true,
            qrCode: qrCodeString,
            connected: false,
            message: 'Escaneie o QR Code para conectar'
        });
    } else {
        res.json({
            success: false,
            message: 'QR Code ainda n√£o foi gerado. Aguarde...',
            connected: false
        });
    }
});

// Endpoint para enviar mensagem
app.post('/send-message', async (req, res) => {
    const { number, message } = req.body;
    
    // Valida√ß√£o dos par√¢metros
    if (!number || !message) {
        return res.status(400).json({
            success: false,
            error: 'Par√¢metros "number" e "message" s√£o obrigat√≥rios',
            example: {
                number: "5511999999999",
                message: "Ol√° do Zelar!"
            }
        });
    }
    
    // Verificar se o cliente est√° conectado
    if (!isClientReady || !client) {
        return res.status(503).json({
            success: false,
            error: 'WhatsApp n√£o est√° conectado',
            message: 'Escaneie o QR Code primeiro ou aguarde a reconex√£o'
        });
    }
    
    try {
        // Formatar n√∫mero para o formato do WhatsApp
        let formattedNumber = number.toString().replace(/\D/g, '');
        
        // Adicionar c√≥digo do pa√≠s se n√£o tiver (assumindo Brasil)
        if (!formattedNumber.startsWith('55') && formattedNumber.length === 11) {
            formattedNumber = '55' + formattedNumber;
        }
        
        const chatId = formattedNumber + '@c.us';
        
        console.log(`üì§ Tentando enviar mensagem para ${formattedNumber}: "${message}"`);
        
        // Verificar se o n√∫mero existe no WhatsApp
        const isRegistered = await client.isRegisteredUser(chatId);
        if (!isRegistered) {
            console.log(`‚ö†Ô∏è N√∫mero ${formattedNumber} n√£o est√° registrado no WhatsApp`);
            return res.status(400).json({
                success: false,
                error: 'N√∫mero n√£o est√° registrado no WhatsApp',
                number: formattedNumber
            });
        }
        
        // Enviar mensagem
        const sentMessage = await client.sendMessage(chatId, message);
        
        console.log(`‚úÖ Mensagem enviada com sucesso para ${formattedNumber}`);
        
        res.json({
            success: true,
            message: 'Mensagem enviada com sucesso',
            to: formattedNumber,
            content: message,
            messageId: sentMessage.id.id,
            timestamp: new Date().toISOString()
        });
        
    } catch (error) {
        console.error(`‚ùå Erro ao enviar mensagem para ${number}:`, error.message);
        
        res.status(500).json({
            success: false,
            error: 'Erro interno ao enviar mensagem',
            details: error.message,
            to: number
        });
    }
});

// Endpoint para obter informa√ß√µes do cliente
app.get('/info', (req, res) => {
    if (!isClientReady || !clientInfo) {
        return res.status(503).json({
            success: false,
            error: 'Cliente n√£o est√° conectado'
        });
    }

    res.json({
        success: true,
        clientInfo: clientInfo,
        status: 'connected',
        timestamp: new Date().toISOString()
    });
});

// Endpoint para reiniciar conex√£o
app.post('/restart', async (req, res) => {
    try {
        console.log('üîÑ Reinicializando cliente WhatsApp...');
        
        if (client) {
            await client.destroy();
        }
        
        isClientReady = false;
        qrCodeString = '';
        clientInfo = null;
        
        // Reinicializar ap√≥s um pequeno delay
        setTimeout(() => {
            initializeWhatsApp();
        }, 2000);
        
        res.json({
            success: true,
            message: 'Cliente reiniciado. Aguarde a reconex√£o.',
            timestamp: new Date().toISOString()
        });
        
    } catch (error) {
        console.error('‚ùå Erro ao reiniciar cliente:', error.message);
        res.status(500).json({
            success: false,
            error: 'Erro ao reiniciar cliente',
            details: error.message
        });
    }
});

// =================== INICIALIZA√á√ÉO ===================

async function initializeWhatsApp() {
    try {
        console.log('üöÄ Criando cliente WhatsApp...');
        client = await createClient();
        await setupClientEvents(client);
        console.log('üîÑ Inicializando cliente WhatsApp...');
        await client.initialize();
    } catch (error) {
        console.error('‚ùå Erro ao inicializar WhatsApp:', error.message);
        console.log('üîÑ Tentando novamente em 10 segundos...');
        setTimeout(initializeWhatsApp, 10000);
    }
}

